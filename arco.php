<p>
2.5 A seleção do arco quando se dispõe de um técnico para consultar torna-se mais fácil. Ele irá considerar o comprimento da puxada e a distância entre o olho e base do queixo do arqueiro. Fig. 8</p>

<img src="images/arco/fig8.png" class = "center">


<p>
Quanto maior essa distância, maior margem de alça terá o arqueiro. Isso permitirá selecionar o arco de menor força de puxada adequado a cada pessoa, para iniciar-se no esporte. Sem esse auxílio, você poderá adquirir um arco de puxada fraca e não conseguir graduação de alça para as distâncias menores.</p>
<p>
Então você sempre opinará por um arco com força de puxada próximo do máximo que você possa suportar.</p>
<p>
